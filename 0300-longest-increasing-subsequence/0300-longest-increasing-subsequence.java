class Solution {
    public int lengthOfLIS(int[] nums) {
        if (nums.length == 0) return 0;

        // DP 배열 생성 및 1로 초기화
        int[] dp = new int[nums.length];
        Arrays.fill(dp, 1);

        int maxLen = 1;

        for (int i = 1; i < nums.length; i++) {
            for (int j = 0; j < i; j++) {
                // 증가하는 수열 조건
                if (nums[i] > nums[j]) {
                    dp[i] = Math.max(dp[i], dp[j] + 1);
                }
            }
            maxLen = Math.max(maxLen, dp[i]);
        }

        return maxLen;
    }
}